/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:

    string path2string(vector<int> &path) {
        string ans = "";
        for (int i = 0; i < path.size(); i++) {
            ans += to_string(path[i]);
            if (i != path.size() - 1) {
                ans += "->";
            }
        }
        return ans;
    }

    void recursion(TreeNode *root, vector<int> &path, vector<string> &ans) {
        path.push_back(root->val);
        if (root->left == nullptr && root->right == nullptr) {
            string s = path2string(path);
            ans.push_back(s);
            return;
        }
        if (root->left) {
            recursion(root->left, path, ans);
            path.pop_back();
        }
        if (root->right) {
            recursion(root->right, path, ans);
            path.pop_back();
        }
    }


    vector<string> binaryTreePaths(TreeNode *root) {
        vector<string> ans;
        vector<int> path;
        recursion(root, path, ans);
        return ans;
    }
};
