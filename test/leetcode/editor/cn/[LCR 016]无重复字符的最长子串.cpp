#include <bits/stdc++.h>

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    int lengthOfLongestSubstring(string s) {
        vector<int> window(128, 0);
        int l = 0, r = 0;
        int res = 0;
        while (r < s.size()) {
            char c = s[r++];
            if (window[c]++ > 0) {
                res = max(res, r - l - 1);
                while (window[c] > 1) {
                    char d = s[l++];
                    window[d]--;
                }
            }
        }
        res = max(res, r - l);
        return res;

    }
};
