#include <bits/stdc++.h>

using namespace std;

//leetcode submit region begin(Prohibit modification and deletion)
class Solution {
public:
    int count[32] = {0};
    int ans = 0;
    int singleNumber(vector<int> &nums) {
        for (int i = 0; i < nums.size(); i++) {
            for (int j = 0; j < 32; j++) {
                count[j] += nums[i] & 1;
                count[j] %= 3;
                nums[i] >>= 1;
            }
        }
        for (int i = 0; i < 32; i++) {
            ans <<= 1;
            ans |= count[31 - i];
        }
        return ans;

    }
};
