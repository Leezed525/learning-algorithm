class Solution {
public:
    int threeSumClosest(vector<int> &nums, int target) {
        sort(nums.begin(), nums.end());

        int closest = INT_MAX;
        int minDiff = INT_MAX;

        for (int i = 0; i < nums.size(); i += 1) {
            if (i > 0 && nums[i] == nums[i - 1]) continue;
            int l = i + 1;
            int r = nums.size() - 1;
            while (l < r) {
                int sum = nums[i] + nums[l] + nums[r];
                int diff = abs(sum - target);
                if (diff == 0) return sum;
                if (diff < minDiff) {
                    minDiff = diff;
                    closest = sum;
                }
                if (sum < target) {
                    l += 1;
                } else if (sum > target) {
                    r -= 1;
                }
            }
        }
        return closest;

    }
};
