class Solution {
public:
    string addBinary(string a, string b) {
        stack<char> st;
        int i = a.size() - 1;
        int j = b.size() - 1;


        int sum = 0;
        while (i >= 0 || j >= 0) {
            if (i >= 0) {
                sum += a[i] - '0';
                i--;
            }
            if (j >= 0) {
                sum += b[j] - '0';
                j--;
            }
            st.push(sum % 2 + '0');
            sum /= 2;
        }

        if (sum) {
            st.push('1');
        }

        string res = "";
        while (!st.empty()) {
            res += st.top();
            st.pop();
        }

        return res;

    }
};
