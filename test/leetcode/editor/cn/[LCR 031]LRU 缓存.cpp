class LRUCache {
public:

    const static int maxn = 10010;

    struct node {
        int key, val;
        node *next, *prev;

        node() {
            key = 0;
            val = 0;
            prev = nullptr;
            next = nullptr;
        }

        node(int _key, int _val) {
            key = _key;
            val = _val;
            next = nullptr;
            prev = nullptr;
        }
    };

    int index = -1;
    node *_hash[maxn];
    int max_length, cur_length = 0;
    node *head, *tail;

    LRUCache(int capacity) {
        max_length = capacity;
        for (int i = 0; i < maxn; i += 1) _hash[i] = nullptr;
        head = new node();
        tail = new node();
        head->next = tail;
        tail->prev = head;
    }

    int get(int key) {
        if (_hash[key] == nullptr) return -1;
        node *cur = _hash[key];
        change_to_head(cur);
        return cur->val;
    }

    void put(int key, int value) {
        if (_hash[key] != nullptr) {
            _hash[key]->val = value;
            change_to_head(_hash[key]);
            return;
        }
        if (cur_length < max_length) {
            cur_length += 1;
        } else {
            //åˆ é™¤æœ€åä¸€ä¸ªèŠ‚ç‚¹
            node *t = tail->prev;
            _hash[t->key] = nullptr;
            t->prev->next = t->next;
            t->next->prev = t->prev;
            delete t;
        }
        node *tmp = new node(key, value);
        tmp->next = head->next;
        head->next->prev = tmp;
        tmp->prev = head;
        head->next = tmp;
        _hash[key] = tmp;
    }

    void change_to_head(node *cur) {
        node *p = cur->prev;
        node *n = cur->next;
        //å°†curæŠ½å‡º
        p->next = n;
        n->prev = p;
        //å°†curæ¥åˆ°å¤´ç»“ç‚¹ä¸‹
        n = head->next;
        n->prev = cur;
        cur->next = n;
        cur->prev = head;
        head->next = cur;
    }
};

