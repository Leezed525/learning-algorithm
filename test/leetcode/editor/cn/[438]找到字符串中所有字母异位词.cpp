class Solution {
public:
    vector<int> findAnagrams(string s, string p) {
        map<char, int> need;
        for (int i = 0; i < p.size(); i++) {
            need[p[i]]++;
        }
        int p_len = p.size();
        vector<int> res;
        for (int i = 0; i < s.size(); i += 1) {
            need[s[i]]--;
            if (i >= p_len - 1) {
                for (auto it = need.begin(); it != need.end(); it++) {
                    if (it->second != 0) {
                        break;
                    }
                    if (it == prev(need.end())) {
                        res.push_back(i - p_len + 1);
                    }
                }
                need[s[i - p_len + 1]]++;
            }
        }
        return res;
    }
};
