class Solution {
public:
    vector<string> res;
    vector<int> path;

    void dfs(string &s, int start) {
        if (path.size() == 4) {
            if (start == s.size()) {
                string ip;
                for (int i = 0; i < 4; i++) {
                    ip += to_string(path[i]);
                    if (i < 3) ip += ".";
                }
                res.push_back(ip);
            }
            return;
        }
        if (start == s.size()) return;
        for (int i = start; i < s.size() && i < start + 3; i += 1) {
            if(s[i] == '0' && i == start){
                path.push_back(0);
                dfs(s, i + 1);
                path.pop_back();
                break;
            }else{
                int num = 0;
                for (int j = start; j <= i; j++) {
                    num = num * 10 + s[j] - '0';
                }
                if (num <= 255) {
                    path.push_back(num);
                    dfs(s, i + 1);
                    path.pop_back();
                } else {
                    break;
                }
            }
        }
    }


    vector<string> restoreIpAddresses(string s) {
        dfs(s, 0);
        return res;
    }
};
