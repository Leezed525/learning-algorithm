/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Codec {
public:

    // Encodes a tree to a single string.
    string serialize(TreeNode *root) {
        queue<TreeNode *> q;
        string res = "[";
        if (root == nullptr) return res + "]";
        q.push(root);
        while (!q.empty()) {
            TreeNode *cur = q.front();
            q.pop();
            if (cur == nullptr) {
                res += "null,";
            } else {
                res += to_string(cur->val) + ",";
                q.push(cur->left);
                q.push(cur->right);
            }
        }

        res[res.size() - 1] = ']'; // æ›¿æ¢æœ€åä¸€ä¸ªé€—å·ä¸ºå³æ‹¬å·
        return res;
    }

    // Decodes your encoded data to tree.
    TreeNode *deserialize(string data) {

        cout << data << endl;
        if (data == "[]") return nullptr;
        data = data.substr(1, data.size() - 2); // å»æ‰å‰åçš„ä¸­æ‹¬å·
        int l = 0, r = 0;
        queue<TreeNode *> needChildNodes;
        //æ‰¾åˆ°ç¬¬ä¸€ä¸ªrootèŠ‚ç‚¹çš„å€¼
        while (r < data.size() && data[r] != ',') r++;
        string rootVal = data.substr(l, r - l);
        l = r + 1;
        r = l;
        TreeNode *root = new TreeNode(stoi(rootVal));
        needChildNodes.push(root);
        while (r < data.size()) {
            TreeNode *cur = needChildNodes.front();
            needChildNodes.pop();
            //æ‰¾åˆ°å½“å‰çš„ä¸¤ä¸ªå­èŠ‚ç‚¹
            for (int i = 0; i < 2; i++) {
                while (r < data.size() && data[r] != ',') r++;
                string childVal = data.substr(l, r - l);
                l = r + 1;
                r = l;
                if (childVal == "null") {
                    if (i == 0) cur->left = nullptr;
                    else cur->right = nullptr;
                } else {
                    TreeNode *childNode = new TreeNode(stoi(childVal));
                    if (i == 0) cur->left = childNode;
                    else cur->right = childNode;
                    needChildNodes.push(childNode);
                }
            }
        }

        return root;


    }
};

